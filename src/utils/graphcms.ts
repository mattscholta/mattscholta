import { GraphQLClient } from 'graphql-request';

import {
  GRAPHCMS_ADMIN,
  GRAPHCMS_TOKEN,
  GRAPHCMS_URL
} from '@src/config/settings';

/**
 * @name graphcms
 * @description GraphCMS client
 */
export const graphcms = new GraphQLClient(GRAPHCMS_URL, {
  headers: {
    Authorization: `Bearer ${GRAPHCMS_TOKEN}`
  }
});

/**
 * @name graphcmsAdmin
 * @description GraphCMS admin client
 */
export const graphcmsAdmin = new GraphQLClient(GRAPHCMS_ADMIN, {
  headers: {
    Authorization: `Bearer ${GRAPHCMS_TOKEN}`
  }
});
